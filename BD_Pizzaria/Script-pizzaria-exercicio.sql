INNER JOIN VENDEDOR V ON V.COD_VENDEDOR = PV.COD_VENDEDOR
INNER JOIN FORMA_PAGAMENTO FP ON FP.COD_FORMA_PAGAMENTO = PV.COD_FORMA_PAGAMENTO
INNER JOIN ITEM_PEDIDO_VENDA IPV ON IPV.COD_PEDIDO_VENDA = PV.COD_PEDIDO_VENDA
INNER JOIN PRODUTO P ON P.COD_PRODUTO = IPV.COD_PRODUTO
INNER JOIN TIPO_PRODUTO TP ON TP.COD_TIPO_PRODUTO = P.COD_TIPO_PRODUTO;

-- Atividade INNER JOIN - PIZZARIA - Douglas Coimbra Laass
-- Relação dos valores de cada pedido realizado por cada Fornecedor
SELECT pc.VLR_PEDIDO_COMPRA, f.NOME AS NOME_FORNECEDOR
FROM PEDIDO_COMPRA pc 
INNER JOIN FORNECEDOR f ON f.COD_FORNECEDOR = pc.COD_FORNECEDOR;

-- Relação de produtos e valores de venda com descrição do Produto
SELECT p.NOME AS NOME_PRODUTO, p.VLR_PRODUTO_VENDA, tp.DES_TIPO_PRODUTO
FROM PRODUTO p 
INNER JOIN TIPO_PRODUTO tp ON tp.COD_TIPO_PRODUTO = p.COD_TIPO_PRODUTO ;

-- Relação dos pedidos de venda, os produtos comprados nesses pedidos, e os clientes que fizeram os pedidos
SELECT pv.COD_PEDIDO_VENDA, pv.DATA_PEDIDO, c.NOME AS NOME_CLIENTE, ipv.QTD_ITEM_PEDIDO_VENDA,
    p.NOME AS NOME_PRODUTO
FROM PEDIDO_VENDA pv
INNER JOIN ITEM_PEDIDO_VENDA ipv ON pv.COD_PEDIDO_VENDA = ipv.COD_PEDIDO_VENDA
INNER JOIN PRODUTO p ON ipv.COD_PRODUTO = p.COD_PRODUTO
INNER JOIN CLIENTE c ON pv.COD_CLIENTE = c.COD_CLIENTE;

-- Relação dos clientes, as datas dos pedidos e os produtos comprados em cada pedido
SELECT c.NOME AS NOME_CLIENTE, p.NOME AS NOME_PRODUTO, pv.DATA_PEDIDO
FROM CLIENTE c
INNER JOIN PEDIDO_VENDA pv ON c.COD_CLIENTE = pv.COD_CLIENTE
INNER JOIN PRODUTO p ON ipv.COD_PRODUTO = p.COD_PRODUTO
INNER JOIN ITEM_PEDIDO_VENDA ipv ON pv.COD_PEDIDO_VENDA = ipv.COD_PEDIDO_VENDA;

-- Relação dos clientes, as datas dos pedidos de venda e os nomes dos vendedores que realizaram a cada pedido
SELECT c.NOME AS NOME_CLIENTE, pv.DATA_PEDIDO, v.NOME AS NOME_VENDEDOR
FROM CLIENTE c
INNER JOIN PEDIDO_VENDA pv ON c.COD_CLIENTE = pv.COD_CLIENTE
INNER JOIN VENDEDOR v ON pv.COD_VENDEDOR = v.COD_VENDEDOR;

-- Relação de todos os vendedores e os pedidos que cada vendedor fez
SELECT v.NOME AS NOME_VENDEDOR, pv.COD_PEDIDO_VENDA
FROM VENDEDOR v
LEFT JOIN PEDIDO_VENDA pv ON v.COD_VENDEDOR = pv.COD_VENDEDOR;

-- Relação de todos os clientes e os pedidos que cada cliente fez com seus valores e datas
SELECT pv.COD_CLIENTE , c.NOME AS NOME_CLIENTE, pv.VLR_PEDIDO_VENDA, pv.DATA_PEDIDO  
FROM PEDIDO_VENDA pv 
RIGHT JOIN CLIENTE c ON c.COD_CLIENTE = pv.COD_CLIENTE;


